    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        int totalHari = 0;
        try{
            if (displayTahun.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Masukkan input bulan", "Masukkan input bulan", JOptionPane.WARNING_MESSAGE);
            }
            else{
                int tahun = Integer.parseInt(displayTahun.getText());
                

                if (displayBulan.getSelectedItem().equals("Februari")){
                    if (((tahun%4 == 0) && (tahun%100 == 0)) || (tahun%400 == 0)){
                        totalHari = 29;
                    }
                    else{
                        totalHari = 28;
                    }
                }

                else if (displayBulan.getSelectedItem().equals("April")||
                    displayBulan.getSelectedItem().equals("Juni")||
                    displayBulan.getSelectedItem().equals("September")||
                    displayBulan.getSelectedItem().equals("November")){
                    totalHari = 30;
                }
                else{                    
                totalHari = 31;
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Input yang anda masukan salah");
        }
            label.setText("Jumlah hari pada bulan");
            labelHasil.setText(displayBulan.getSelectedItem() + "\n" + " tahun " + displayTahun.getText()+" adalah "+ totalHari + " hari");
        
        
        
    }//GEN-LAST:event_hitungActionPerformed

