    private void displayTahunKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_displayTahunKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if ( !((Character.isDigit(c)) || (c == KeyEvent.VK_DELETE) || (c == KeyEvent.VK_BACK_SPACE))){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Masukkan input berupa angka");
            evt.consume();
        }
    }//GEN-LAST:event_displayTahunKeyTyped

